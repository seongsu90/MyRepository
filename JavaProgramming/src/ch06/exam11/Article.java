package ch06.exam11;

public class Article {
	int no;
	String title;
	String writer;
	String content;
	int hitNum;
	
	Article(){}
	Article(int no,String title,String writer, String content,int hitNum)
	{
		this.no = no;
		this.title=title;
		this.writer=writer;
		this.content = content;
		this.hitNum=hitNum;
		
	}
}
